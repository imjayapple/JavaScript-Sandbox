/*
Built-in Operators
within JavaScript
*/

// Addition
console.log(5 + 4);

// Subtraction
console.log(10 - 9);

// Multiplication
console.log(5 * 4);

// Division
console.log(100 / 10);

// Remainder
console.log(20 % 6);

// Increment
let x = 5;
x++; // x becomes 6

// Decrement
let y = 10;
y--; // y becomes 9